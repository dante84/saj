package mx.gob.se.sraaj.comunes.bean;

public class ResponsableBean {
	
	   private String idResponsable;
	   private String nombre;
	   private String descripcionPuesto;
	   
	   public String getIdResponsable() { return idResponsable; }
	   
	   public void setIdResponsable(String idResponsable) { this.idResponsable = idResponsable; }
	   
	   public String getNombre() { return nombre; }
	   
	   public void setNombre(String nombre) { this.nombre = nombre; }
	   
	   public String getDescripcionPuesto() { return descripcionPuesto; }
	   
	   public void setDescripcionPuesto(String descripcionPuesto) { this.descripcionPuesto = descripcionPuesto;	}

}
